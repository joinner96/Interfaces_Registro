    private void jButtonRegistrarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarseActionPerformed
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            String usuario = "root";
            String password = "";
            String url = "jdbc:mysql://localhost:3306/register";

            try (Connection conexion = DriverManager.getConnection(url, usuario, password)) {
                String nombre = jTextFieldNombre.getText();
                String apellidos = jTextFieldApellidos.getText();
                String correo = jTextFieldCorreo.getText();
                String passwordField = new String(jPasswordFieldPass1.getPassword());

                String query = "INSERT INTO usuarios (nombre, apellidos, correo, password) values('" + nombre + "', '" + apellidos + "', '" + correo + "', '" + passwordField + "')";

                try (Statement statement = conexion.createStatement()) {
                    statement.executeUpdate(query);
                }

                JOptionPane.showMessageDialog(null, "Registro Exitoso");

            } catch (SQLException ex) {
                Logger.getLogger(Interfaces.class.getName()).log(Level.SEVERE, null, ex);
                System.out.println("Error al conectarse");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Interfaces.class.getName()).log(Level.SEVERE, null, ex);
        }// TODO add your handling code here:
    }//GEN-LAST:event_jButtonRegistrarseActionPerformed

